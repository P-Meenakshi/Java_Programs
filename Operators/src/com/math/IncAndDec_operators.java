package com.math;

public class IncAndDec_operators {
    public static void main(String[] args){
        int a= 10;
        int r;
        System.out.println("value of a:"+ a);
        r=++a;
        System.out.println("value after inc:"+ r);
        r=--a;
        System.out.println("valus dec:"+ r);

    }
}
