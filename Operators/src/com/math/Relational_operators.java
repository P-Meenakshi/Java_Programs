package com.math;

public class Relational_operators {
    public static void main(String[] args) {
        int a = 34;
        int b = 17;
        boolean r;
        r = a == b;
        System.out.println("a==b?" + r);
        r = a != b;
        System.out.println("a!=b?" + r);
        r = a < b;
        System.out.println("a<b?" + r);
        r = a<=b;
        System.out.println("a<=b?"+r);
        r = a>b;
        System.out.println("a>b?"+r);
        r = a>=b;
        System.out.println("a>=b?"+r);
    }
}
